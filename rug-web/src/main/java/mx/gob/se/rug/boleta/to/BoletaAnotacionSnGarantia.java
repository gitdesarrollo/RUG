package mx.gob.se.rug.boleta.to;

public class BoletaAnotacionSnGarantia {

	private Integer idTramite;
	private String autoridadInstruye ;
	private String anotacion;
	private String vigencia;
	private String fechaInscripcion;
	private String nombreUsuario;
	private String perfil;
	private String folioOtorgante;
	
	
	public Integer getIdTramite() {
		return idTramite;
	}
	public void setIdTramite(Integer idTramite) {
		this.idTramite = idTramite;
	}
	public String getAutoridadInstruye() {
		return autoridadInstruye;
	}
	public void setAutoridadInstruye(String autoridadInstruye) {
		this.autoridadInstruye = autoridadInstruye;
	}
	public String getAnotacion() {
		return anotacion;
	}
	public void setAnotacion(String anotacion) {
		this.anotacion = anotacion;
	}
	public String getVigencia() {
		return vigencia;
	}
	public void setVigencia(String vigencia) {
		this.vigencia = vigencia;
	}
	public String getFechaInscripcion() {
		return fechaInscripcion;
	}
	public void setFechaInscripcion(String fechaInscripcion) {
		this.fechaInscripcion = fechaInscripcion;
	}
	public String getNombreUsuario() {
		return nombreUsuario;
	}
	public void setNombreUsuario(String nombreUsuario) {
		this.nombreUsuario = nombreUsuario;
	}
	public String getPerfil() {
		return perfil;
	}
	public void setPerfil(String perfil) {
		this.perfil = perfil;
	}
	public String getFolioOtorgante() {
		return folioOtorgante;
	}
	public void setFolioOtorgante(String folioOtorgante) {
		this.folioOtorgante = folioOtorgante;
	}

	
	
}
