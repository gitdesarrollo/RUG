package mx.gob.se.rug.firma.to;

public class FirmaTO {
	private String cadenaOriginalSolicitante;
	private String selloSolicitante;
	private String numeroSerieSolicitante;
	private String autoridadCertificadoraSolicitante;
	
	private String cadenaOriginalSE;
	private String selloSE;
	private String numeroSerieSE;
	
	private String timeStamp;
	
	public String getCadenaOriginalSolicitante() {
		return cadenaOriginalSolicitante;
	}
	public void setCadenaOriginalSolicitante(String cadenaOriginalSolicitante) {
		this.cadenaOriginalSolicitante = cadenaOriginalSolicitante;
	}
	public String getSelloSolicitante() {
		return selloSolicitante;
	}
	public void setSelloSolicitante(String selloSolicitante) {
		this.selloSolicitante = selloSolicitante;
	}
	public String getNumeroSerieSolicitante() {
		return numeroSerieSolicitante;
	}
	public void setNumeroSerieSolicitante(String numeroSerieSolicitante) {
		this.numeroSerieSolicitante = numeroSerieSolicitante;
	}
	public String getAutoridadCertificadoraSolicitante() {
		return autoridadCertificadoraSolicitante;
	}
	public void setAutoridadCertificadoraSolicitante(
			String autoridadCertificadoraSolicitante) {
		this.autoridadCertificadoraSolicitante = autoridadCertificadoraSolicitante;
	}
	public String getCadenaOriginalSE() {
		return cadenaOriginalSE;
	}
	public void setCadenaOriginalSE(String cadenaOriginalSE) {
		this.cadenaOriginalSE = cadenaOriginalSE;
	}
	public String getSelloSE() {
		return selloSE;
	}
	public void setSelloSE(String selloSE) {
		this.selloSE = selloSE;
	}
	public String getNumeroSerieSE() {
		return numeroSerieSE;
	}
	public void setNumeroSerieSE(String numeroSerieSE) {
		this.numeroSerieSE = numeroSerieSE;
	}
	public String getTimeStamp() {
		return timeStamp;
	}
	public void setTimeStamp(String timeStamp) {
		this.timeStamp = timeStamp;
	}
	

}
